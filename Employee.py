class Employee:
    def __init__(self, fname, lname, status, department, title,
                 dob, gender, payType, email, address, zip, dependents, admin, password):
        
        self.fname = fname
        self.lname = lname
        self.status = status
        self.department = department
        self.title = title
        self.dob = dob
        self.gender = gender
        self.payType = payType
        self.email = email
        self.address = address
        self.zip = zip
        self.dependents = dependents
        self.admin = admin
        self.password = password
